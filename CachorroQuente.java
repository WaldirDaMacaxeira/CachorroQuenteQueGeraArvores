package CachorroQuenteQueGeraArvores;

public class CachorroQuente {
	public String proteina;
	public String queijo;
	public String bebida;
	public boolean maionese;
	public boolean ketchup;
	public boolean ovo;
	public boolean batataFrita;
	
	public CachorroQuente(String proteina, String queijo, String bebida, boolean maionese, boolean ketchup, boolean ovo, boolean batataFrita) {
		this.proteina = proteina;
		this.queijo = queijo;
		this.bebida = bebida;
		this.maionese = maionese;
		this.ketchup = ketchup;
		this.ovo = ovo;
		this.batataFrita = batataFrita;
	}
	
}
